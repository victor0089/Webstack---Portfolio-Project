// Register User
if (isset($_POST['register'])) {
    $full_name = $_POST["full_name"];
    $username = $_POST["username"];
    $email = $_POST["email"];
    $password = $_POST['password'];
    $hashed_password = password_hash($password, PASSWORD_DEFAULT);
    $active = isset($_POST["active"]) ? 1 : 0; // Check if active checkbox is checked

    

    $sql = "INSERT INTO users (full_name, username, email,  password, active) VALUES ('$full_name', '$hashed_password', '$username', '$email', '$active')";
    if ($conn->query($sql) === TRUE) {
        echo "Registration successful";
    } else {
        echo "Error: " . $sql . "<br>" . $conn->error;
    }
}

// Login User
if (isset($_POST['login'])) {
    $username = $_POST['username'];
    $password = $_POST['password'];

    $sql = "SELECT * FROM users WHERE username='$username'";
    $result = $conn->query($sql);

    if ($result->num_rows == 1) {
        $row = $result->fetch_assoc();
        if (password_verify($password, $row['password'])) {
            $_SESSION['username'] = $username;
            header("Location: dashboard.php"); // Redirect to dashboard after successful login
        } else {
            echo "Invalid username or password";
        }
    } else {
        echo "User not found";
    }
}

